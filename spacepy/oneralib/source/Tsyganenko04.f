       APHI2=((1.D0-XK2*0.5D0)*ELK-ELE)/XKRHO12

       AP_04=A1*APHI1+A2*APHI2
       IF (PROX) AP_04=AP_04*SINT/SINT1   !   LINEAR INTERPOLATION, IF TOO CLOSE TO THE Z-AXIS
